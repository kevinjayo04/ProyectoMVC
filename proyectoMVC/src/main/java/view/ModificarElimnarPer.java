    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int filaSeleccionada = tblPersonas.getSelectedRow();

        // Verificar si se ha seleccionado una fila
        if (filaSeleccionada != -1) {
            // Obtener el DNI de la persona seleccionada
            String dniPersona = (String) tblPersonas.getValueAt(filaSeleccionada, 0);

            // Mostrar mensaje de confirmacion para eliminar
            int confirmacion = JOptionPane.showConfirmDialog(this, "¿Estas seguro de que deseas eliminar a esta persona?",
                    "Confirmacion de eliminacion", JOptionPane.YES_NO_OPTION);

            // Si el usuario selecciona Sí
            if (confirmacion == JOptionPane.YES_OPTION) {
                // Verificar si la persona tiene vehículos asociados
                boolean tieneAsociacion = personCtrl.tieneVehiculoAsociado(dniPersona);

                if (tieneAsociacion) {
                    // Mostrar advertencia de asociación
                    JOptionPane.showMessageDialog(this, "No se puede eliminar a esta persona porque esta asociada a un vehiculo. "
                            + "Desasocie la persona del vehiculo antes de eliminarla.");
                } else {
                    // Llamar al metodo para eliminar la persona por DNI
                    personCtrl.eliminarPerSeleccionada(dniPersona);

                    // Mostrar un mensaje confirmando que la persona fue eliminada
                    JOptionPane.showMessageDialog(this, "Persona eliminada correctamente.");

                    // Recargar la tabla con los datos actualizados
                    tblPersonas.setModel(personCtrl.recargaPersonasTb());

                    txtNombre.setText("");
                    txtDni.setText("");

                    rdFemenino.setSelected(false);
                    rdMasculino.setSelected(false);

                    tblPersonas.clearSelection();
                }
            }
        } else {
            // Mostrar un mensaje de si no se ha seleccionado ninguna fila
            JOptionPane.showMessageDialog(this, "Por favor, selecciona una persona para eliminar.");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

