    private void tblPersonasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPersonasMouseClicked
        // TODO add your handling code here:
        int filaSeleccionada = tblPersonas.getSelectedRow();
        if (filaSeleccionada != -1) {
            String dni = (String) tblPersonas.getValueAt(filaSeleccionada, 0);
            String nombre = (String) tblPersonas.getValueAt(filaSeleccionada, 1);
            String genero = (String) tblPersonas.getValueAt(filaSeleccionada, 2);

            txtNombre.setText(nombre);
            txtDni.setText(dni);
            if (genero.equals("MASCULINO")) {
                rdMasculino.setSelected(true);
                rdFemenino.setSelected(false);
            } else if (genero.equals("FEMENINO")) {
                rdFemenino.setSelected(true);
                rdMasculino.setSelected(false);

            }
        }
    }//GEN-LAST:event_tblPersonasMouseClicked

